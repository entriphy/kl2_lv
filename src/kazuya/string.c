static void kzSTR_Draw_Core(DVECTOR *sxy, u32 sz, KZCOLOR *col, short int *str, void (*ds_func)(DVECTOR *, u32, DVECTOR *, DVECTOR *, KZCOLOR *)) {
    DVECTOR pos;
    GIM_FORM **table;
    GIM_FORM *form;
    s16 code;
    s16 font;

    pos.vx = sxy->vx;
    pos.vy = sxy->vy;
    font = *str++;
    table = FontTables[font];

    for (code = *str; code != 0; code = *++str) {
        if (code < -999) {
            pos.vx = sxy->vx;
            pos.vy -= 1000;
            pos.vy -= code;
        } else if (code <= 0) {
            pos.vx -= code;
        } else {
            form = table[code - 1];
            kz_Set_Tex0_Imd(form->tex0);
            ds_func(&pos, sz, &form->wh, &form->uv, col);
            pos.vx += form->wh.vx;
        }
    }
}

void kzSTR_Draw(DVECTOR *sxy, u32 sz, KZCOLOR *col, s32 str_no) {
    kzSTR_Draw_Core(sxy, sz, col, Strings[str_no], kz_Set_SprtUV);
}

void kzSTR_Draw_Eff(DVECTOR *sxy, u32 sz, KZCOLOR *col, s32 str_no) {
    kzSTR_Draw_Core(sxy, sz, col, Strings[str_no], kz_Set_SprtUV_Eff);
}

void kzNUM_Draw(DVECTOR *sxy, u32 sz,  KZCOLOR *col, s32 font, s32 figure, u32 num) {
    static s16 str[256];
    s32 i;

    str[0] = font;
    for (i = 0; i < figure; i++) {
        str[figure - i] = num % 10 + 1;
        num /= 10;
    }
    str[figure + 1] = 0;
    kzSTR_Draw_Core(sxy, sz, col, str, kz_Set_SprtUV);
}

