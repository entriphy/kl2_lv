void kzCHR_Draw_Direct(DVECTOR *sxy, u32 sz, KZCOLOR *col, s32 font_no, s16 code) {
    GIM_FORM **table;
    GIM_FORM *form;

    if (code <= 0) {
        return;
    }

    table = FontTables[font_no];
    form = table[code - 1];
    kz_Set_Tex0_Imd(form->tex0);
    kz_Set_SprtUV(sxy, sz, &form->wh, &form->uv, col);
}

