    prog = (id >> 8) & 0x7F
    bank = (id >> 15) & 1
    grp = (id >> 16) & 0xF
    unk = (id >> 20) & 0xF
    flag = (id >> 24) & 0xFF
    bendh = (id >> 32) & 0xFFF
    bendl = (id >> 44) & 0xFFF
    return Vag(splt, prog, bank, grp, unk, flag, bendh, bendl)

